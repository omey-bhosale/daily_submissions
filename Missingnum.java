class Solution {
    int MissingNumber(int array[], int n) {
        // Your Code Here
        Arrays.sort(array);
        int num=1;
        int i;
        for(i=0;i<n-1;i++)
        {
            if(num==array[i])
            {
                num++;
            }
            else
            {
                return num;
            }
        }
        return num;
        
    }
}
